PE0 => Left button (MultiFn board)

PE2 => Right button (MultiFn board)

PD4 => Clock for ADC chip (PWM)

PD2 => Confirm button (MultiFn board)

PD3 => Interrupt SPI

Frequency SPI: 16 Mhz

Frequency DUO: 84 Mhz
Baudrate CAN: 250kHz

CAN messages:
- Joystick Position: ID 100

!! When sending a message from Arduino to Node1, the ID is wrong !!

